#unsorted list
unsorted_list = ["sanchari","manav","kshtitj","shorya","keshav"]

print("Unsorted list(Original): ")
print(unsorted_list)

#sorted list - temporary
print(sorted(unsorted_list))

#sorted list - permanent
unsorted_list.sort()
print(unsorted_list)

#revereing the sorted list
unsorted_list.reverse()
print(unsorted_list)

#length of the list

print(len(unsorted_list))
